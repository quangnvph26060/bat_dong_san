@push('scripts')
    <script>
        // Hàm hiển thị popup
        function showPopup() {
            const popup = document.getElementById('pum-121');
            if (popup) {
                popup.style.display = 'block'; // Hiện popup
                // Thay đổi giá trị opacity để tạo hiệu ứng
                setTimeout(() => {
                    popup.style.opacity = '1';
                }, 0);
            }
        }

        // Ẩn popup sau khi nhấn nút đóng
        document.querySelector('.pum-close').addEventListener('click', function() {
            const popup = document.getElementById('pum-121');
            if (popup) {
                popup.style.display = 'none'; // Ẩn popup
                popup.style.opacity = '0'; // Thiết lập opacity về 0
            }
        });

        // Hiện popup lần đầu tiên sau 1 phút
        setTimeout(() => {
            showPopup();
        }, 60000); // 60000 milliseconds = 1 minute

        // Thiết lập timer để hiển thị popup lại sau mỗi 1 phút
        setInterval(() => {
            showPopup();
        }, 60000); // 60000 milliseconds = 1 minute
    </script>
@endpush
